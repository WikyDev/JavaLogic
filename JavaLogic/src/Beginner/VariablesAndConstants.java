package Beginner;

public class VariablesAndConstants {
    public static void main(String[] args) {
        //Variables
        String name = "Wiky";
        System.out.println(name + " " + ";)");
        name = "Wilson";

        int age;
        //Constants
        age = 25;
        System.out.println(age);

        var apellido = "Ospina";
        System.out.println(apellido);
        //Constants

        final String EMAIL = "wiky@gmail.com";
        // email = "hola";
        System.out.println(EMAIL);
    }
}
